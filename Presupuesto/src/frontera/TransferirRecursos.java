    void llenarEmisor(){
        emisorCombo.removeAllItems();
        for(Object[] o:controls.getRubro(nombrepresupuesto)){
            emisorCombo.addItem(o[0]);
        }
    }
    
    void llenarReceptor(){
        receptorCombo.removeAllItems();
        for(Object[] o:controls.getRubro(nombrepresupuesto)){
            if(!((String)emisorCombo.getSelectedItem()).equals(o[0])){
                receptorCombo.addItem(o[0]);
            }
        }
    }
