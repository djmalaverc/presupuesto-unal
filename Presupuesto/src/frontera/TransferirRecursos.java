    private void AceptarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarBotonActionPerformed
        if (emisorCombo.getSelectedIndex() == -1) {
            emisorCombo.addItem("");
            emisorCombo.setSelectedItem("");

        }
        if (receptorCombo.getSelectedIndex() == -1) {
            receptorCombo.addItem("");
            receptorCombo.setSelectedItem("");
        }
        if(montoTexto.getText().equals("")||montoTexto.getText()==null){
            montoTexto.setText("");
        }
        this.dispose();
    }//GEN-LAST:event_AceptarBotonActionPerformed

